/* Compute binary logarithm
 * Returns the binary (base-2) logarithm of x.
 */
#include <mynum/math.h>

namespace mynum {
namespace math {

Float log2(const Float& x) {
  return mynum::log2(x);
}

Float log2f(const Float& x) {
  return mynum::log2(x);
}

Float log2l(const Float& x) {
  return mynum::log2(x);
}

} // namespace math
} // namespace mynum